void Start_ADC_Read(void)
{ 
  memset(au16_AdcDMABuffValues, 0, sizeof(au16_AdcDMABuffValues));
  memset(au16_AdcFilteredValues, 0, sizeof(au16_AdcFilteredValues));
  HAL_ADCEx_Calibration_Start(&hadc1, ADC_SINGLE_ENDED);
  HAL_ADC_Start_DMA(&hadc1, (uint32_t*)au16_AdcDMABuffValues, ADC_DMA_BUFF_LEN);
}

void Stop_ADC(void)
{
  HAL_ADC_Stop_DMA(&hadc1);
  memset(au16_AdcDMABuffValues, 0, sizeof(au16_AdcDMABuffValues));
  memset(au16_AdcFilteredValues, 0, sizeof(au16_AdcFilteredValues));
}

void AdcFilterCompute(uint8_t u8_Block)
{
  uint32_t au32_AdcMoyBuff[NUM_ADC_INPUTS];
  uint8_t i, j;

  // copy DMA buffer to moy buffer
  for (j = 0; j < NUM_ADC_INPUTS; j++)
  {
    au16_AdcBuff4Moy[u8_AdcBuff4Moy_pt][j] = au16_AdcDMABuffValues[j + (u8_Block * NUM_ADC_INPUTS)];
  }

  u8_AdcBuff4Moy_pt++;
  u8_AdcBuff4Moy_pt %= ADC_BUFF_4_MOY_VAL_SIZE;
  // compute moy of each channel
  memset(au32_AdcMoyBuff, 0, sizeof(au32_AdcMoyBuff));
  for (i = 0; i < ADC_BUFF_4_MOY_VAL_SIZE; i++)
  {
    for (j = 0; j < NUM_ADC_INPUTS; j++)
      au32_AdcMoyBuff[j] += au16_AdcBuff4Moy[i][j];
  }
  for (j = 0; j < NUM_ADC_INPUTS; j++)
  {
    au32_AdcMoyBuff[j] /= ADC_BUFF_4_MOY_VAL_SIZE;
    au16_AdcFilteredValues[j] = (uint16_t)au32_AdcMoyBuff[j];
  }
}

void HAL_ADC_ConvCpltCallback(ADC_HandleTypeDef* hadc)
{
  /* Prevent unused argument(s) compilation warning */
  UNUSED(hadc);
  AdcFilterCompute(1);
}

void HAL_ADC_ConvHalfCpltCallback(ADC_HandleTypeDef* hadc)
{
  /* Prevent unused argument(s) compilation warning */
  UNUSED(hadc);
  AdcFilterCompute(0);
}


